import { serve } from "https://deno.land/std@0.190.0/http/server.ts";
import { createClient } from "https://esm.sh/@supabase/supabase-js@2.39.0";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

interface GetEmailRequest {
  userId: string;
}

serve(async (req) => {
  // Handle CORS preflight requests
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const supabaseClient = createClient(
      Deno.env.get('SUPABASE_URL') ?? '',
      Deno.env.get('SUPABASE_SERVICE_ROLE_KEY') ?? ''
    );

    // Get the user from the Authorization header
    const authHeader = req.headers.get('Authorization');
    if (!authHeader) {
      throw new Error('No authorization header');
    }

    const token = authHeader.replace('Bearer ', '');
    const { data: { user }, error: authError } = await supabaseClient.auth.getUser(token);

    if (authError || !user) {
      throw new Error('Invalid or expired token');
    }

    // Check if user is admin
    const { data: roleData, error: roleError } = await supabaseClient
      .from('user_roles')
      .select('role')
      .eq('user_id', user.id)
      .eq('role', 'admin')
      .single();

    const isSpecialAdmin = user.email === 'admin@rise-fitness.com';

    if (!isSpecialAdmin && (roleError || !roleData)) {
      throw new Error('User is not authorized as admin');
    }

    const { userId }: GetEmailRequest = await req.json();

    if (!userId) {
      throw new Error('Missing userId');
    }

    // Get user email from auth.users using admin client
    const { data: userData, error: userError } = await supabaseClient.auth.admin.getUserById(userId);

    if (userError) {
      console.error('Error fetching user email:', userError);
      throw new Error(`Failed to fetch user email: ${userError.message}`);
    }

    const email = userData?.user?.email || '';

    console.log(`Successfully fetched email for user ${userId}`);

    return new Response(
      JSON.stringify({ email }),
      {
        headers: {
          ...corsHeaders,
          'Content-Type': 'application/json',
        },
      }
    );
  } catch (error: any) {
    console.error('Error in get-member-email function:', error);
    return new Response(
      JSON.stringify({ error: error.message }),
      {
        status: 400,
        headers: {
          ...corsHeaders,
          'Content-Type': 'application/json',
        },
      }
    );
  }
});